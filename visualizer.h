    float lastX = 0.f, lastY = 0.f;
    void static mouseMoveCB(GLFWwindow* window, double xpos, double ypos) {
        auto app = static_cast<TriangulationVisualizer*>(glfwGetWindowUserPointer(window));
        xpos /= INITIAL_WIDTH / 2;
        ypos /= INITIAL_HEIGHT / 2;

        if (!ImGui::GetIO().WantCaptureMouse && app->mouseHeld) {
            float dx = xpos - app->lastX;
            float dy = ypos - app->lastY;
            app->offset += glm::vec2(dx, dy);
        }
        app->lastX = xpos;
        app->lastY = ypos;
    }

