    void createVertexBuffer() {
        const VkDeviceSize vertexBufferSize  = sizeof(Vertex) * p.points.size();
        const int num_threads = /*omp_get_max_threads();*/ 1;
        indexBufferSize = 0;
        for (int i = 0; i < num_threads; i++) {
            for (auto& chain : p.dt.chains[i]) {
                indexBufferSize += chain.size() + 1;
            }
        }
        indexBufferSize *= sizeof(uint32_t);

        // Create GPU-local buffers
        createBuffer(vertexBufferSize, VK_BUFFER_USAGE_TRANSFER_DST_BIT | VK_BUFFER_USAGE_VERTEX_BUFFER_BIT, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, vertexBuffer);
        createBuffer(indexBufferSize, VK_BUFFER_USAGE_TRANSFER_DST_BIT | VK_BUFFER_USAGE_INDEX_BUFFER_BIT, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, indexBuffer);



        // Memory requirements for vertex & index buffers
        VkMemoryRequirements vertexMemReq, indexMemReq;
        vkGetBufferMemoryRequirements(device, vertexBuffer, &vertexMemReq);
        vkGetBufferMemoryRequirements(device, indexBuffer, &indexMemReq);

        // Align index offset after vertex buffer
        constexpr VkDeviceSize vertexOffset = 0;
        const VkDeviceSize vertexEnd = vertexOffset + vertexBufferSize;
        const VkDeviceSize indexOffset = alignUp(vertexEnd, indexMemReq.alignment);
        const VkDeviceSize deviceMemorySize = indexOffset + indexBufferSize;

        // Allocate combined device-local memory
        VkMemoryAllocateInfo deviceOnlyAllocInfo{};
        deviceOnlyAllocInfo.sType = VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO;
        deviceOnlyAllocInfo.allocationSize = deviceMemorySize;
        deviceOnlyAllocInfo.memoryTypeIndex = findMemoryType(
            vertexMemReq.memoryTypeBits & indexMemReq.memoryTypeBits,
            VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);

        if (vkAllocateMemory(device, &deviceOnlyAllocInfo, nullptr, &vtxIdxDeviceOnlyMemory) != VK_SUCCESS)
            throw std::runtime_error("failed to allocate device-local memory!");

        vkBindBufferMemory(device, vertexBuffer, vtxIdxDeviceOnlyMemory, vertexOffset);
        vkBindBufferMemory(device, indexBuffer, vtxIdxDeviceOnlyMemory, indexOffset);


        // Create staging buffers
        VkBuffer vtxStagingBuffer, idxStagingBuffer;
        createBuffer(vertexBufferSize, VK_BUFFER_USAGE_TRANSFER_SRC_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, vtxStagingBuffer);
        createBuffer(indexBufferSize, VK_BUFFER_USAGE_TRANSFER_SRC_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, idxStagingBuffer);

        // Get staging buffer requirements and calculate offsets
        VkMemoryRequirements vtxStagingReq, idxStagingReq;
        vkGetBufferMemoryRequirements(device, vtxStagingBuffer, &vtxStagingReq);
        vkGetBufferMemoryRequirements(device, idxStagingBuffer, &idxStagingReq);

        const VkDeviceSize stagingVertexOffset = 0;
        const VkDeviceSize stagingVertexEnd = stagingVertexOffset + vertexBufferSize;
        const VkDeviceSize stagingIndexOffset = alignUp(stagingVertexEnd, idxStagingReq.alignment);
        const VkDeviceSize stagingAllocSize = stagingIndexOffset + indexBufferSize;

        VkDeviceMemory stagingBufferMemory;
        VkMemoryAllocateInfo stagingAllocInfo{};
        stagingAllocInfo.sType = VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO;
        stagingAllocInfo.allocationSize = stagingAllocSize;
        stagingAllocInfo.memoryTypeIndex = findMemoryType(
            vtxStagingReq.memoryTypeBits & idxStagingReq.memoryTypeBits,
            VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT);

        if (vkAllocateMemory(device, &stagingAllocInfo, nullptr, &stagingBufferMemory) != VK_SUCCESS)
            throw std::runtime_error("failed to allocate staging buffer memory!");

        vkBindBufferMemory(device, vtxStagingBuffer, stagingBufferMemory, stagingVertexOffset);
        vkBindBufferMemory(device, idxStagingBuffer, stagingBufferMemory, stagingIndexOffset);

        // Copy vertex data
        void* data;
        vkMapMemory(device, stagingBufferMemory, stagingVertexOffset, vertexBufferSize, 0, &data);
#ifdef DOUBLE_REAL
        Vertex* vtx_data = static_cast<Vertex*>(data);
        for (int i = 0; i < p.points.size(); i++) {
            auto& v = p.points[i];
            vtx_data[i].pos = glm::vec2(v.x, v.y);
        }
#else
        memcpy(data, p.points.data(), (size_t)vertexBufferSize);
#endif
        vkUnmapMemory(device, stagingBufferMemory);

        // Copy index data
        vkMapMemory(device, stagingBufferMemory, stagingIndexOffset, indexBufferSize, 0, &data);
        uint32_t* casted_data = static_cast<uint32_t*>(data);
        for (int i = 0; i < num_threads; i++) {
            for (auto& chain : p.dt.chains[i]) {
                memcpy(casted_data, chain.data(), chain.size() * sizeof(uint32_t));
                casted_data += chain.size();
                *casted_data = 0xFFFFFFFF; // End of chain
                casted_data++;
            }
        }
        vkUnmapMemory(device, stagingBufferMemory);

        // Copy from staging to device-local
        copyBuffer(vtxStagingBuffer, vertexBuffer, vertexBufferSize);
        copyBuffer(idxStagingBuffer, indexBuffer, indexBufferSize);

        // Clean up staging buffers
        vkDestroyBuffer(device, vtxStagingBuffer, nullptr);
        vkDestroyBuffer(device, idxStagingBuffer, nullptr);
        vkFreeMemory(device, stagingBufferMemory, nullptr);
}


